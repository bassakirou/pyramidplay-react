// Fonction de recherche
export const searchContent = (query: string) => {
  const lowerQuery = query.toLowerCase();

  const songs = allSongs.filter(
    (song) =>
      song.title.toLowerCase().includes(lowerQuery) ||
      song.artistName.toLowerCase().includes(lowerQuery) ||
      song.albumName.toLowerCase().includes(lowerQuery),
  );

  const albumsResult = albums.filter(
    (album) =>
      album.title.toLowerCase().includes(lowerQuery) ||
      album.artistName.toLowerCase().includes(lowerQuery),
  );

  const artistsResult = artists.filter((artist) =>
    artist.name.toLowerCase().includes(lowerQuery),
  );

  return { songs, albums: albumsResult, artists: artistsResult };
};

